package help

// InArray func
func InArray(val string, array []string) (ok bool) {
	for _, data := range array {
		if ok = data == val; ok {
			return
		}
	}
	return
}

// Diff func
func Diff(a, b []string) (diff []string) {
	m := make(map[string]bool)

	for _, item := range b {
		m[item] = true
	}

	for _, item := range a {
		if _, ok := m[item]; !ok {
			diff = append(diff, item)
		}
	}
	return
}

// ArrUniqueInt func
func ArrUniqueInt(intSlice []int) []int {
	keys := make(map[int]bool)
	list := []int{}
	for _, entry := range intSlice {
		if _, value := keys[entry]; !value {
			keys[entry] = true
			list = append(list, entry)
		}
	}
	return list
}

